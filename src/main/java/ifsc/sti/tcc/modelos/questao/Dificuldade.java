package ifsc.sti.tcc.modelos.questao;

public class Dificuldade {

	private Long id;
	private Long idQuestao;
	private Integer muitoFacil;
	private Integer facil;
	private Integer media;
	private Integer dificil;
	private Integer muitoDificil;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getIdQuestao() {
		return idQuestao;
	}
	public void setIdQuestao(Long idQuestao) {
		this.idQuestao = idQuestao;
	}
	public Integer getMuitoFacil() {
		return muitoFacil;
	}
	public void setMuitoFacil(Integer muitoFacil) {
		this.muitoFacil = muitoFacil;
	}
	public Integer getFacil() {
		return facil;
	}
	public void setFacil(Integer facil) {
		this.facil = facil;
	}
	public Integer getMedia() {
		return media;
	}
	public void setMedia(Integer media) {
		this.media = media;
	}
	public Integer getDificil() {
		return dificil;
	}
	public void setDificil(Integer dificil) {
		this.dificil = dificil;
	}
	public Integer getMuitoDificil() {
		return muitoDificil;
	}
	public void setMuitoDificil(Integer muitoDificil) {
		this.muitoDificil = muitoDificil;
	}
	
		
}
